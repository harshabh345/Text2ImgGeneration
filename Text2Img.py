    col_key = COLOR_NAMES[idx % len(COLOR_NAMES)]
    base_c  = np.array(PALETTE[col_key])
    petal_c = tuple(np.clip(
        base_c + rng.integers(-30, 30, 3), 0, 255).tolist())

    img  = Image.new("RGB", (256, 256), bg_col)
